import math # inicialmente eu faço a importação da biblioteca math para poder usar a função fatorial.

num = int(input("Digite um número de 1 a 10: ")) # Depois eu solicito ao usuário que digite um número entre 1 e 10, tomando o cuidado de transformar em inteiro.
print(math.factorial(num)) # exibo o resultado do fatorial da variável num com a função math.factorial(num).

